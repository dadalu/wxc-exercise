    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    public class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode() {
        }

        TreeNode(int val) {
            this.val = val;
        }

        TreeNode(int val, TreeNode left, TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }

    class Solution {
        public int amountOfTime(TreeNode root, int start) {
            Map<Integer, List<Integer>> graph = new HashMap<>();
            dfs(graph, root);
            Queue<int[]> queue = new ArrayDeque<>();
            queue.offer(new int[]{start, 0});
            Set<Integer> visited = new HashSet<>();
            visited.add(start);
            int time = 0;
            while (!queue.isEmpty()) {
                int[] arr = queue.poll();
                int nodeVal = arr[0];
                time = arr[1];
                for (int childval : graph.get(nodeVal)) {
                    if(visited.add(childval)){
                        queue.offer(new int[]{childval, time + 1});
                    }
                }
            }
            return time;
        }

        public void dfs(Map<Integer, List<Integer>> graph, TreeNode node) {
            graph.putIfAbsent(node.val, new ArrayList<>());
            for (TreeNode child : Arrays.asList(node.left, node.right)) {
                if (child != null) {
                    graph.get(node.val).add(child.val);
                    graph.putIfAbsent(child.val, new ArrayList<>());
                    graph.get(child.val).add(node.val);
                    dfs(graph,child);
                }
            }
        }
    }
