    public Node connect(Node root){
        if (root == null) {
            return root;
        }
        Node l = root;
        while (l.left != null) {
            Node head = l;
            while (head != null) {
                head.left.next = head.right;
                if (head.next != null) {
                    head.right.next = head.next.left;
                }
                head = head.next;
            }
            l = l.left;
        }
        return root;
    }
